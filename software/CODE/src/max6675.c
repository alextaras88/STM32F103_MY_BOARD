float ReadData_6675(void)
{
	uint16_t data;
	CS_1_LOW;
	data = Spi_Write_Read_Data16(SPI1, 0xFFFF);	
	CS_1_HIGH;                                                                        
	data = (0x0FFF &(data>>3));	
	
	return  (float)(data/4.096);		
}
