#include "mcp4725.h"

/****************************************************************************************************/
/* MCP4725 12 bit DAC  i2c                                                   											  */
/****************************************************************************************************/
void Write_mcp4725(uint8_t addr, uint16_t data, uint8_t config){
	
  
  Start_i2c(I2C1);
	Write_addr_i2c(I2C1,addr,0);
	Write_data_i2c(I2C1, config);
	Write_data_i2c(I2C1, (uint8_t)(data/16));
	Write_last_data_i2c(I2C1, (uint8_t)(data %16)<<4);
	Stop_i2c(I2C1);
}
